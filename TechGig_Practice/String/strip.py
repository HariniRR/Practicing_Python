# all whitespaces have been stripped from the beginning as well as end of the string.
def main():
    inp=input()
    print(inp.strip())
main()

#all whitespaces have been stripped from the end of the string.
def main():
    inp=input()
    print(inp.rstrip())
main()

#all whitespaces have been stripped from the beginning of the string.
def main():
    inp=input()
    print(inp.lstrip())
main()